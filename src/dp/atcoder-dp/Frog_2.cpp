#include <bits/stdc++.h>
using namespace std;
using ll = long long;

vector<int> dp;

int sol(int idx, int n, int k, vector<int> &h)
{
    if (idx == n - 1)
    {
        return 0;
    }
    if (dp[idx] != -1)
    {
        return dp[idx];
    }
    int cost = INT_MAX;

    for (int i = 1; i <= k; i++)
    {
        if (idx + i < n)
        {
            cost = min(cost, (sol(idx + i, n, k, h) + abs(h[idx] - h[idx + i])));
        }
    }

    return dp[idx] = cost;
}

void solve()
{
    int n, k;
    cin >> n >> k;
    dp.resize(n, -1);
    vector<int> h(n, 0);
    for (int i = 0; i < n; i++)
    {
        cin >> h[i];
    }

    cout << sol(0, n, k, h);
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while (t--)
    {
        solve();
    }
}


#include <bits/stdc++.h>
using namespace std;
using ll = long long;

vector<int> dp;
int sol(int i, vector<int> &h, int n)
{
    if (i == n - 1)
    {
        return 0;
    }

    if (dp[i] != -1)
    {
        return dp[i];
    }
    int cost = INT_MAX;
    cost = min(cost, (sol(i + 1, h, n) + abs(h[i] - h[i + 1])));
    if (i + 2 < n)
    {
        cost = min(cost, (sol(i + 2, h, n) + abs(h[i] - h[i + 2])));
    }

    return dp[i] = cost;
}

void solve()
{
    int n;
    cin >> n;
    dp.resize(n, -1);
    vector<int> h(n, 0);
    for (int i = 0; i < n; i++)
    {
        cin >> h[i];
    }
    cout << sol(0, h, n) << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while (t--)
    {
        solve();
    }
}


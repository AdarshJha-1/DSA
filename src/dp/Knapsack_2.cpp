#include <bits/stdc++.h>
using namespace std;
using ll = long long;

vector<vector<ll>> dp;

long long sol(vector<ll> &w, vector<ll> &v, ll currW, ll idx)
{
    if (idx < 0)
    {
        return 0;
    }
    if (dp[idx][currW] != -1)
    {
        return dp[idx][currW];
    }
    ll skip = sol(w, v, currW, idx - 1);
    ll take = 0;
    if (currW - w[idx] >= 0)
    {
        take = sol(w, v, currW - w[idx], idx - 1) + v[idx];
    }

    return dp[idx][currW] = max(take, skip);
}

void solve()
{
    ll N = 0, W = 0;
    cin >> N >> W;
    dp.resize(N, vector<ll>(W + 1, -1));
    vector<ll>
        w(N),
        v(N);
    for (ll i = 0; i < N; i++)
    {
        cin >> w[i];
        cin >> v[i];
    }

    cout << sol(w, v, W, N - 1) << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    // cin >> t;
    while (t--)
    {
        solve();
    }
}


void solve()
{
    int N;
    cin >> N;
    vector<vector<int>> C(N, vector<int>(N, 0));
    for (int i = 0; i < N; i++)
    {
        for (int j = i + 1; j < N; j++)
        {
            cin >> C[i][j];
            C[j][i] = C[i][j];
        }
    }

    for (int a = 0; a < N; a++)
    {
        for (int b = a + 1; b < N; b++)
        {
            for (int c = b + 1; c < N; c++)
            {
                if (C[a][b] + C[b][c] < C[a][c])
                {
                    cout << "Yes\n";
                    return;
                }
            }
        }
    }
    cout << "No\n";
}


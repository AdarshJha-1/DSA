void solve()
{
    int n, k;
    cin >> n >> k;
    string s;
    cin >> s;

    vector<int> arr(26, 0);
    for (const auto &c : s)
    {
        arr[c - 'a']++;
    }

    int oddCount = 0;
    for (const auto &a : arr)
    {
        if (a % 2 != 0)
        {
            oddCount++;
        }
    }

    if (oddCount > k + 1)
    {
        no;
    }
    else
    {
        yes;
    }
}


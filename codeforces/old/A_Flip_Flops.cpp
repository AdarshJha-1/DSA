void solve()
{
    int n, c, k;
    cin >> n >> c >> k;
    vector<int> arr(n, 0);
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    sort(arr.begin(), arr.end());

    for (int i = 0; i < n; i++)
    {
        if (arr[i] <= c)
        {
            int takeFromK = c - arr[i];
            if (k < takeFromK)
            {
                c += k + arr[i];
                k -= k;
            }
            else
            {
                c += takeFromK + arr[i];
                k -= takeFromK;
            }
        }
    }

    cout << c << endl;
}


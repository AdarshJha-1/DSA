void solve()
{
    int n, k;
    cin >> n >> k;
    vector<int> arr(n);

    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    sort(arr.begin(), arr.end());

    int maxLen = 1;
    int currLen = 1;

    for (int i = 1; i < n; i++)
    {
        if (arr[i] - arr[i - 1] <= k)
        {
            currLen++;
        }
        else
        {
            currLen = 1;
        }
        maxLen = max(maxLen, currLen);
    }

    cout << n - maxLen << "\n";
}


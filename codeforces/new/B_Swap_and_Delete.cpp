void solve()
{
    string s;
    cin >> s;
    ll c0 = 0, c1 = 0;
    for (const auto &c : s)
    {
        if (c == '0')
        {
            c0++;
        }
        else
        {
            c1++;
        }
    }
    int t_sz = 0;
    for (const auto &c : s)
    {
        if (c == '0' && c1 > 0)
        {
            c1--;
            t_sz++;
        }
        else if (c == '1' && c0 > 0)
        {
            c0--;
            t_sz++;
        }
        else
        {
            break;
        }
    }

    cout << s.size() - t_sz << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
}

